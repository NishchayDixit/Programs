/* WAP to print your address i) using single print, ii) using multiple print */

public class Print_Statement_1_3 {
	public static void main(String[] args) {
		
		// Using single print
		System.out.print("DIET, \nRajkot-Morb Highway, \nRajkot.\n");
		
		System.out.println("--------------------");
		
		// Using multiple print
		System.out.println("DIET,");
		System.out.println("Rajkot-Morb Highway,");
		System.out.println("Rajkot.");
	}
}
